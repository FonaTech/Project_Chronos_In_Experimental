def set_current_lang(lang: str):
    global _current_lang
    _current_lang = lang


def get_current_lang() -> str:
    return _current_lang


def t(key: str, lang: str = None) -> str:
    l = lang or _current_lang
    return TRANSLATIONS.get(l, TRANSLATIONS["en"]).get(key, key)


def register_translatable(component, label_key: str):
    _translatable_components.append((component, label_key))


def build_language_update(lang: str):
    set_current_lang(lang)
    updates = []
    for comp, key in _translatable_components:
        text = t(key, lang)
        # gr.Button uses `value`; gr.Tab and most others use `label`
        if isinstance(comp, gr.Button):
            updates.append(gr.update(value=text))
        else:
            updates.append(gr.update(label=text))
    return updates


def get_registered_components():
    return [c for c, _ in _translatable_components]
